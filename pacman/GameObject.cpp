#include "GameObject.h"
void GameObject::setX(int newX)
{
	this->xPos = newX;
}
//----------------------------------------------------------------------------------------//
void GameObject::setY(int newY)
{
	this->yPos = newY;
}
//----------------------------------------------------------------------------------------//
int GameObject::getX()
{
	return this->xPos;
}
//----------------------------------------------------------------------------------------//
int GameObject::getY()
{
	return this->yPos;
}
//----------------------------------------------------------------------------------------//
void GameObject::printPos() const
{
	cout << '(' << this->xPos << ',' << this->yPos << ')' << endl;
}
//----------------------------------------------------------------------------------------//